fun main(){
    val val01:Byte = 1;
    val val02:Short = 12345;
    val val03:Int = 123456789;
    val val04:Long = 12345678900000;
    val val05:Char = 'a';
    val val06:String = "Hello,Kotlin";

    println(val01)
    println(val02)
    println(val03)
    println(val04)
    println(val05)
    println(val06)
}

